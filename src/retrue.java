    private void txtuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtuiActionPerformed
       try {
            ResultSet serch = db.serch("select * from issure_book where member_id='"+txtui.getText().toUpperCase()+"'and stat=1");

            if(serch.next()){
               
                txtb1.setText(serch.getString("book_id1"));
                b2.setText(serch.getString("book_id2"));
                isd.setText(serch.getString("issuru_date"));
                red.setText(serch.getString("retun_date"));
             
          
           
          SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");       
        String curunt = new SimpleDateFormat("yyyy-MM-dd ").format(new Date());
        txtrd.setText(curunt);
           long cd= sdf.parse(curunt).getTime();
           String retun =red.getText();
            long rd= sdf.parse(retun).getTime();
            long dif =Math.abs(sdf.parse(retun).getTime()-sdf.parse(curunt).getTime());
            long convert =TimeUnit.MILLISECONDS.convert(dif, TimeUnit.SECONDS);
            long convert1=TimeUnit.DAYS.convert(dif, TimeUnit.MILLISECONDS);
           
            
           if(rd>cd){
              txtkeep.setText("0");
              jTextField5.setText("0");
           }else{
              txtkeep.setText(String.valueOf(convert1)+" Day ");
              jTextField5.setText("Rs."+String.valueOf(convert1*5)+".00");
           }
            }

           
          
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_txtuiActionPerformed

